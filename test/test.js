describe('removeIgnoredWordsFromStr', function() {
    it('should remove the word app from str', function () {
	var str = "app is app"
	var ignoredWords = ["app"];
	filteredStr = removeIgnoredWordsFromStr(str,ignoredWords)
	assert.equal(filteredStr, " is ");
    });
    it('should remove the word app and google from str', function () {
	var str = "app is app and google is google"
	var ignoredWords = ["app", "google"];
	filteredStr = removeIgnoredWordsFromStr(str,ignoredWords)
	assert.equal(filteredStr, " is  and  is ");
    });
});

describe('wordCloudForString', function() {
    it('should give back 15 results', function () {
	var wordCloud = wordCloudForString(doc);
	assert.equal(wordCloud.length, 15);
    });
});
